     public BufferedImage createBufferedImage(int[][] mtx) {
        int alto = mtx.length;
        int ancho = mtx[0].length;
        BufferedImage bufferedImage = new BufferedImage(ancho, alto, BufferedImage.TYPE_INT_RGB);
        for (int i = 0; i < alto; i++) {
            for (int j = 0; j < ancho; j++) {
                bufferedImage.setRGB(j, i, mtx[i][j]);
            }
        }
        return bufferedImage;
    }

     
    public void printMascara(double [][]msk){
        int sz = msk.length;
        String txt = "<html>";
        for(int i = 0; i < sz; i++){
            for(int j = 0; j < sz; j++){
                txt += msk[i][j] +"&nbsp;| ";
            }
            txt += "<br/>";
        }
        txt+="</html>";
        txtMsk.setText(txt);
    }

