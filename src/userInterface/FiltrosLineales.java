    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Aplicar transformacion
        if(jComboBox1.getSelectedIndex() == 0){
            showMessageDialog(rootPane, "Seleccione una opcion");
            return;
        }
        int idx = jComboBox1.getSelectedIndex();
        if(idx == 2){
            Canny canny = new Canny();
            double low  = (double) jSpinner1.getValue();
            double high = (double) jSpinner2.getValue();
            double ans[][] = canny.applyCannyEdgeDetector(image1.getArray(), low , high);
            imageAns = new ImageDataByChannels(image1, image1.getCurrChannel(), ans);
            ipAns = showContent.drawImage(imageAns, ipAns, imagePanel2);
            hpAns = showContent.drawHisto(imageAns, hpAns, histo2, Color.MAGENTA);
            txtMsk.setText("");
            return;
        }
        
        double [][]currentMascara;
        if(idx == 1){
            KernelGaussiano kg = new KernelGaussiano();
            currentMascara = kg.calcularKernel((double)jSpinner1.getValue(), (double)jSpinner2.getValue());
        }else{
            currentMascara = mascaras[idx-3];
        }
        printMascara(currentMascara);
        boolean normalizar = jRadioButton1.isSelected();
        double newData[][]= Convolution.applyConvolution(image1.getArray(), currentMascara, normalizar);
        imageAns = new ImageDataByChannels(image1, image1.getCurrChannel(), newData);
        ipAns = showContent.drawImage(imageAns, ipAns, imagePanel2);
        hpAns = showContent.drawHisto(imageAns, hpAns, histo2, Color.MAGENTA);
    }//GEN-LAST:event_jButton2ActionPerformed

